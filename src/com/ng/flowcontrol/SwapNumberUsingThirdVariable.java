package com.ng.flowcontrol;

public class SwapNumberUsingThirdVariable {

	public static void main(String[] args) {
		
		int a=5;
		int b=6;
		int c=0;
		
		System.out.println("Before Swapping the value of a: "+a+" and the value of b:"+b);
		
		c=a;
		a=b;
		b=c;
		
		System.out.println("After Swappimh the value of a:"+a+" and the value of b: "+b);
		
		
		
		
	}
}
